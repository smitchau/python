# Write a Python program to check whether an element exists within a tuple.

tup = ('python', 'c++', 'java', 'php', 'html')  # Given tuple of programming languages

if 'python' in tup:  # Checking if 'python' exists in the tuple
    print('element exists within the tuple')  # If 'python' exists, print this message
else:
    print('element does not exist within the tuple')  # If 'python' doesn't exist, print this message
