'''
Write a Python program to find the maximum and minimum numbers 
from the specified decimal numbers.
'''

# List of decimal numbers
decimal_numbers = [3.14, 2.718, 1.618, 4.669, 5.432]

# Find the maximum and minimum numbers
max_number = max(decimal_numbers)  # Find the maximum number using max()
min_number = min(decimal_numbers)  # Find the minimum number using min()

# Display the maximum and minimum numbers
print(f"The maximum number is: {max_number}")
print(f"The minimum number is: {min_number}")
