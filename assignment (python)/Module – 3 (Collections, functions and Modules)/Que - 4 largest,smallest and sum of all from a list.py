'''
Write a Python function to get the largest number, smallest num and sum 
of all from a list.
'''
li = [4, 7, 2, 9, 10, 8, 3, 75, 20, 5, 54, 34, 24, 63]  # Given list of numbers

# Printing the largest number in the list using the max() function
print("largest number is:", max(li))

# Printing the smallest number in the list using the min() function
print("smallest number is:", min(li))

# Printing the sum of all numbers in the list using the sum() function
print("sum:", sum(li))

