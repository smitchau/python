#Que - 53 How can you pick a random item from a list or tuple? 

import random  # Importing the random module

# Define a list and a tuple
lis1 = [10, 30, 39, 43, 54, 76, 234, 456, 65, 21]
tup1 = ('smit', 'nikhil', 'python', 'java', 'purshottam')

# Print a random item from the list lis1 using random.choice()
print(random.choice(lis1))

# Print a random item from the tuple tup1 using random.choice()
print(random.choice(tup1))

